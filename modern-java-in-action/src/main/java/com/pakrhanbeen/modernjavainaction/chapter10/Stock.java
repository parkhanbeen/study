package com.pakrhanbeen.modernjavainaction.chapter10;

class Stock {
    private String symbol;
    private String market;

    String getSymbol() {
        return symbol;
    }

    void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    String getMarket() {
        return market;
    }

    void setMarket(String market) {
        this.market = market;
    }
}
